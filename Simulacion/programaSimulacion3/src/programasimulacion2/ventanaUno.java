    private void Button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button1ActionPerformed

        /*Realizamos un parseo de los datos obtenidos de los TextBox*/
        int semilla              = Integer.parseInt(textSemilla.getText());
        int constMultiplicativa  = Integer.parseInt(textIterar.getText());
        int constAditiva         = Integer.parseInt(textFuncion.getText());
        int modulo               = Integer.parseInt(textModulo.getText());
        int periodo              = Integer.parseInt(textPeriodo.getText());
        double salida []            = new double[periodo];     

        /*Quemamos la funcion X0 = (aXn-ยก + c)mod(m)*/
        int funcionTrancision = 0;
        
        
        for(int i=0; i<periodo; i++)
        {
            funcionTrancision = (constMultiplicativa * semilla + constAditiva) % modulo;
            semilla=funcionTrancision;
            salida[i]=funcionTrancision;
            String cadena=String.valueOf(funcionTrancision); 
            TextAreaX1.setText(TextAreaX1.getText() + "\n " + cadena);
          
            
        }
        
        for(int k = 0; k < periodo; k++)
        {
            double resta=modulo-1;
            double result =(salida[k]/resta);
            String salidaCadena= String.valueOf(result);
            TextAreaU1.setText(TextAreaU1.getText() + "\n " + salidaCadena);
        }    
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_Button1ActionPerformed

